# U7. OrderedDict 的取捨：保序但更吃記憶體（1.7）
# 這個範例探討了 OrderedDict (有序字典) 背後的運作代價與記憶體取捨 (Trade-off)。

from collections import OrderedDict

# 建立一個 OrderedDict
d = OrderedDict()

# 依序寫入資料，OrderedDict 會牢牢記住這個插入的順序：先 foo，再 bar。
d["foo"] = 1
d["bar"] = 2

# ---------------------------------------------------------
# 深入解析：為什麼它會「更吃記憶體」？
# ---------------------------------------------------------
# 一般的字典 (dict) 底層是一個單純的「雜湊表 (Hash Table)」。
# 但是，OrderedDict 為了要記住資料「誰先來、誰後到」，
# 它在底層偷偷維護了一個【雙向鏈結串列 (Doubly Linked List)】！
#
# 每當你新增一個鍵值對 (Key-Value) 時，它除了把資料存進雜湊表，
# 還會建立一個「節點 (Node)」串在鏈結串列的尾端，並且用指標 (Pointer) 把前後節點連起來。
#
# 這些額外的「指標」就是吃掉大量記憶體的元兇！
# 據統計，OrderedDict 消耗的記憶體通常是普通 dict 的 2 倍以上。

# ---------------------------------------------------------
# 實務上的建議 (Python 3.7+ 的時代變化)
# ---------------------------------------------------------
# 1. 如果你要處理「百萬筆」的資料，請盡量避免使用 OrderedDict，否則記憶體可能會爆掉。
# 2. 【重要更新】：從 Python 3.7 開始，內建的普通 `dict` 已經正式保證「會記住插入順序」了！
#    而且它採用了更聰明、更省記憶體的實作方式。
# 3. 那麼現在還需要 OrderedDict 嗎？
#    通常只有在需要它特有的方法時才用，例如：
#    - d.move_to_end('foo')  # 可以隨意把元素移到最前面或最後面
#    - 或是你需要嚴格相容舊版 Python 時。
