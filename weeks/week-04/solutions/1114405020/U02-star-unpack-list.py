# U2. 星號解包為何能處理「不定長」且結果固定是 list（1.2）
# 這個技巧展示了 Python 中「星號運算子 (*)」在解包時的強大特性。
# 星號變數的作用是：「幫我把剩下所有對應不上的元素，全部打包起來帶走！」

# 假設我們有一個使用者的資料紀錄，只有名字與信箱，並沒有電話號碼。
record = ("Dave", "dave@example.com")

# ---------------------------------------------------------
# 星號解包的特性：永遠回傳 List (串列)
# ---------------------------------------------------------
# 1. 第一個變數 name 會拿走 'Dave'
# 2. 第二個變數 email 會拿走 'dave@example.com'
# 3. 接下來的 *phones 會試圖拿走「所有剩下的東西」。
name, email, *phones = record

# 💡 重要觀念一：不會噴錯！
# 在前一個例子 (U1) 中，我們知道如果變數數量與元素對不上會報錯。
# 但因為有了 `*phones`，它就像一個「彈性的垃圾袋」，即使剩下的元素是 0 個，它也能欣然接受，不會引發 ValueError 錯誤。

# 💡 重要觀念二：結果固定是串列 (List)
# 無論原始資料 `record` 是一個 Tuple（如本例）、List，還是一般的字串，
# 被星號 `*` 打包起來的變數，其型態【永遠都會是串列 (List)】！
#
# 所以在這裡，雖然沒有任何電話號碼可以抓，
# phones 依然會是一個合法的空串列，也就是： []
# print("電話列表:", phones)  # 輸出: []
# print("電話列表型態:", type(phones)) # 輸出: <class 'list'>

