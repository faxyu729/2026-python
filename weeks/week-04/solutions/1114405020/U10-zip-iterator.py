# U10. zip 為何只能用一次（1.8）
# 這個範例展示了 Python 中「迭代器 (Iterator)」最核心的特性：【一次性消耗品】。
# 許多新手在將 zip(), map(), filter() 的結果存入變數後，會很驚訝為何第二次使用時變成了空的。
# 這正是因為它們都是迭代器，一旦走到底，就回不去了。

prices = {"A": 2.0, "B": 1.0}

# 呼叫 zip() 將字典的 values 和 keys 打包在一起。
# 注意：此時 z 並「不是」一個 list 陣列 [(2.0, 'A'), (1.0, 'B')]。
# 它是一個「等待被呼叫才會吐出資料的機器 (Iterator)」。
z = zip(prices.values(), prices.keys())

# ---------------------------------------------------------
# 第一次使用：正常運作
# ---------------------------------------------------------
# min() 函式為了要找出最小值，它必須把 z 裡面的每一個元素都「抽出來」看過一遍。
# 這個抽出的過程就像是在抽衛生紙，抽一張少一張。
# 當 min() 找完並回傳 (1.0, 'B') 的時候，z 裡面的資料也已經被徹底抽乾了。
result_min = min(z)  # 成功！z 裡面的元素被消耗完畢


# ---------------------------------------------------------
# 第二次使用：發生意外 (或是無聲無息地回傳錯誤結果)
# ---------------------------------------------------------
# 如果這時候我們又拿同一個 z 丟給 max() 去找最大值：
# max() 會試著去 z 裡面抽資料，但發現「裡面已經空了！什麼都抽不出來」。
# 因為對於空序列無法找出最大值，所以程式會當場拋出 ValueError 錯誤：
# ValueError: max() arg is an empty sequence
# max(z)  # 💥 這裡會報錯

# 💡 實務上的解決方案：
# 解法一：如果資料量不大，在建立 zip 的時候直接把它轉成實體的 list (串列) 儲存起來。
# z_list = list(zip(prices.values(), prices.keys()))
# 這樣 z_list 就是一個實實在在的陣列，想用幾次 min/max 都可以。
#
# 解法二：每次要用到的時候，重新宣告一次 zip。
# result_min = min(zip(prices.values(), prices.keys()))
# result_max = max(zip(prices.values(), prices.keys()))
