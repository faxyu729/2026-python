# R19. 轉換+聚合：生成器表達式（1.19）
# 這個技巧展示了如何在執行「聚合函數」(例如 sum, min, max, join) 的同時，
# 利用「生成器運算式 (Generator Expression)」在當下直接做資料轉換。
# 這樣做最大的好處是優雅，並且比先建立一個大陣列再計算來得「更節省記憶體」。

# ---------------------------------------------------------
# 範例一：數值轉換與總和計算
# ---------------------------------------------------------
nums = [1, 2, 3]

# 計算所有數字的「平方和」
# 傳統可能會寫：sum([x * x for x in nums])  <-- 這會先在記憶體裡建立出 [1, 4, 9] 的暫存串列
# 優雅的寫法：把裡面的中括號 [] 拿掉！
# 這會變成一個生成器 (Generator)，它會「邊產生平方值，邊丟給 sum 加總」，不會產生多餘的串列。
result_sum = sum(x * x for x in nums)


# ---------------------------------------------------------
# 範例二：字串轉換與拼接
# ---------------------------------------------------------
# 假設我們有一個包含多種型態的元組 (字串, 整數, 浮點數)
s = ("ACME", 50, 123.45)

# 如果直接用 ','.join(s) 會發生 TypeError，因為 join 只能拼接「字串」。
# 所以我們在 join 的括號裡面，直接用生成器把每一個元素轉成字串 str(x)。
# 這樣就可以順利拼接成單一字串。
# 結果會是：'ACME,50,123.45'
csv_str = ",".join(str(x) for x in s)


# ---------------------------------------------------------
# 範例三：在複雜資料結構中尋找極值 (min, max)
# ---------------------------------------------------------
portfolio = [{"name": "AOL", "shares": 20}, {"name": "YHOO", "shares": 75}]

# 寫法 A：使用生成器找出「最小的股數」是幾股
# 這會將所有的 shares 數值抽出來，然後找最小值。
# 回傳結果會是純數字： 20
min_shares_value = min(s["shares"] for s in portfolio)

# 寫法 B：使用 key 參數找出「股數最少的那筆資料 (整個字典)」
# 如果你想要知道的不只是「最小是多少」，而是「哪一筆最少」，你應該使用 key 參數！
# min 函數會把 portfolio 裡面的每一個元素(字典) 丟進 lambda 匿名函數去取出 shares 的值來做比較。
# 回傳結果會是那整筆字典： {'name': 'AOL', 'shares': 20}
min_portfolio_item = min(portfolio, key=lambda s: s["shares"])
