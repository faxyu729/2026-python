# R20. ChainMap 合併映射（1.20）
# ChainMap 是用來將多個字典 (字典或映射物件) 「邏輯上」合併在一起的好工具。
# 它不會像 update() 那樣去建立一個新的大字典並複製資料，而是建立一個「觀景窗 (View)」，
# 在搜尋時依序往下找。這在處理「多層級設定」(例如：全域設定檔 -> 使用者設定檔 -> 命令列參數) 時特別好用。

from collections import ChainMap

# 假設我們有兩個字典
a = {"x": 1, "z": 3}
b = {"y": 2, "z": 4}

# 將字典 a 和 b 串聯起來
# 注意順序：a 在前面，b 在後面
c = ChainMap(a, b)

# ---------------------------------------------------------
# 讀取資料的邏輯：從前到後找
# ---------------------------------------------------------
# 當我們要求取 'x' 時：
# 程式會先去字典 a 裡面找，發現有 'x': 1，就直接回傳 1。
print(c["x"])  # 結果：1

# 當我們要求取 'y' 時：
# 程式先去字典 a 裡面找，找不到！接著再去字典 b 裡面找，找到 'y': 2，回傳 2。
print(c["y"])  # 結果：2

# 當兩個字典都有相同的鍵 'z' 時：
# 程式先去字典 a 裡面找，找到了 'z': 3，就直接回傳 3，並且【不會】再往下找了！
# 所以它會遮蔽 (shadow) 後面字典 b 裡面的 'z': 4。
print(c["z"])  # 結果：3 (取到字典 a 的 z)


# ---------------------------------------------------------
# 修改與新增的邏輯：永遠只會動到第一個字典！
# ---------------------------------------------------------
# 這是 ChainMap 一個非常重要（也容易踩坑）的特性：
# 任何透過 ChainMap 進行的修改 (新增、刪除、更改)，都只會影響到「串列中的第一個字典」。

# 測試修改與新增
c["z"] = 100  # 把 z 改成 100
c["w"] = 400  # 新增一個原本沒有的鍵 w

# 我們印出原本的字典 a 看看：
# 結果會發現字典 a 被改變了！ 變成了 {'x': 1, 'z': 100, 'w': 400}
# print("原本的字典 a:", a)

# 但是字典 b 完全不受影響！依然是 {'y': 2, 'z': 4}
# print("原本的字典 b:", b)
