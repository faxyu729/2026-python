# U8. 字典最值為何常用 zip(values, keys)（1.8）
# 當我們在字典中尋找最大值或最小值時，最常遇到的一個痛點就是：
# 「找得到值，卻丟了鍵；找得到鍵，卻不知道值。」
# 這個範例展示了如何巧妙地運用 zip() 反轉結構，一次把 Key 和 Value 帶回家。

prices = {"A": 2.0, "B": 1.0}

# ---------------------------------------------------------
# 痛點一：直接對字典取 min()
# ---------------------------------------------------------
# 如果直接丟進 min()，Python 預設只會拿字典的「鍵 (Key)」來做比較。
# 所以它比較的是 'A' 和 'B'，'A' 的字母順序比較前面，於是回傳 'A'。
# 但我們真正在乎的其實是「價格 (Value)」！所以這樣找完全沒有意義。
# print(min(prices))  # 回傳 'A'


# ---------------------------------------------------------
# 痛點二：對 values() 取 min()
# ---------------------------------------------------------
# 為了比較價格，我們改成從 prices.values() 裡面找最小的。
# 這樣雖然成功找到了最便宜的價格 1.0，
# 但是我們卻不知道這 1.0 是屬於哪一家公司的！(遺失了 Key 的資訊)
# print(min(prices.values()))  # 回傳 1.0


# ---------------------------------------------------------
# 💡 完美解法：使用 zip() 反轉 Key-Value 的順序
# ---------------------------------------------------------
# zip() 就像拉鍊一樣，可以把兩個序列「一一對應」地咬合起來。
# 這裡我們故意把「值 (values)」放在前面，把「鍵 (keys)」放在後面：
# zip([2.0, 1.0], ['A', 'B'])  => 會產生 [(2.0, 'A'), (1.0, 'B')]

# 把這個反轉後的序列丟進 min() 裡面：
# Python 在比較 Tuple (元組) 時，會先從第一個元素開始比。
# 第一個元素現在是「價格」，所以它會先找出最便宜的 1.0。
# 如果價格剛好一樣（平手），它才會去比較第二個元素（公司名稱的字母順序）。
#
# 最後 min() 會把整個 Tuple 原封不動地回傳，
# 這樣我們就可以「同時」拿到最低價格，以及它所屬的公司了！
result = min(zip(prices.values(), prices.keys()))
# print(result)  # 回傳 (1.0, 'B')

# ⚠️ 注意事項 (zip 的一次性特性)：
# zip() 產生的是一個迭代器 (Iterator)，它只能「被消耗一次」。
# 所以你不能把 zip 的結果存進一個變數，然後先呼叫 min() 再呼叫 max()，
# 第二次呼叫時會得到空錯誤！每次需要計算時，都必須重新呼叫一次 zip()。
