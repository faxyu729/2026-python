# U4. heap 為何能高效拿 Top-N（1.4）
# 這個範例展示了 Python 的 heapq 模組 (Heap Queue / 堆積佇列) 的核心概念。
# 為什麼在找「最大或最小的 N 個數字」時，用 Heap 會比用 sort() 排序還要快？
# 因為 sort() 要把所有資料徹頭徹尾排好序，但 Heap 只需要維持一個「最值在頂端」的樹狀結構即可。

import heapq

nums = [5, 1, 9, 2]

# ---------------------------------------------------------
# 第一步：建立 Heap 結構
# ---------------------------------------------------------
# 先複製一份原本的串列，避免改動到原始資料
h = nums[:]

# heapq.heapify() 會在原地 (in-place) 將普通的串列轉換成「Min-Heap (最小堆積)」結構。
# 轉換的時間複雜度是 O(N)，非常快。
heapq.heapify(h)

# 💡 Min-Heap 的核心性質 (最重要的一點！)：
# 轉換完之後，它依然是一個串列，但是它的第一個元素 `h[0]` 【永遠都會是所有數字裡面的最小值】！
# 注意：這不代表整個串列是由小到大完全排好序的 (例如後面的元素可能是 [1, 2, 9, 5])，
# 它只保證「父節點永遠小於等於子節點」，所以最頂端的 `h[0]` 絕對是最小的。
# print("Heapify 後的串列:", h)  # 可能會輸出 [1, 2, 9, 5]


# ---------------------------------------------------------
# 第二步：取出最小值
# ---------------------------------------------------------
# heapq.heappop() 會幫你做兩件事：
# 1. 把它把目前串列裡面的最小值 (也就是 h[0]) 拿出來回傳給你。
# 2. 自動把剩下的數字重新調整，確保新的 h[0] 又是剩下數字裡的最小值。
# 每次 pop 的時間複雜度只有 O(log N)。
m = heapq.heappop(h)

# 因為一開始的陣列是 [5, 1, 9, 2]，所以第一次 pop 出來的絕對是 1。
# print("取出的最小值:", m)  # 輸出: 1

# 如果你連續 pop，就會依序拿到 2, 5, 9，這也就是為什麼 Heap 非常適合用來實作「優先佇列 (Priority Queue)」。
# 如果要找最大的 Top N，可以使用 heapq.nlargest()；
# 如果要找最小的 Top N，可以使用 heapq.nsmallest()。
