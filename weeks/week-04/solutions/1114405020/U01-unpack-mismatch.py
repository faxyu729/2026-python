# U1. 解包失敗的原因：變數數量 ≠ 元素數量（1.1）
# 在 Python 中進行「解包 (Unpacking)」時，最基本且最嚴格的規定就是：
# 等號左邊的「變數數量」，必須【剛剛好等於】等號右邊的「資料元素數量」。

# 這裡有一個包含 2 個元素的元組 (Tuple)
p = (4, 5)

# ---------------------------------------------------------
# 正確的寫法：變數數量 == 元素數量 (2 = 2)
# ---------------------------------------------------------
# 這樣寫是合法的，4 會被放進 x，5 會被放進 y
x, y = p

# ---------------------------------------------------------
# 錯誤的寫法：變數太多 (3 > 2)
# ---------------------------------------------------------
# 如果像下面這樣寫，要求拿出 3 個東西，但來源只有 2 個：
# x, y, z = p
# 程式會拋出錯誤：ValueError: not enough values to unpack (expected 3, got 2)

# ---------------------------------------------------------
# 錯誤的寫法：變數太少 (1 < 2)
# ---------------------------------------------------------
# 如果來源有 2 個東西，但你只準備了 1 個變數來接：
# a = p      # ⚠️ 注意：這樣寫不會出錯，因為這不叫解包，這叫做把整個元組 (4,5) 指派給變數 a。
# a, = p     # ❌ 如果加上逗號強迫解包，就會拋出錯誤：ValueError: too many values to unpack (expected 1)

# 💡 延伸解法：
# 如果遇到變數與元素數量對不上的情況，通常可以引入「星號運算子 (*)」來解決。
# 例如： x, *y = (1, 2, 3) 這樣 y 就會變成一個包含剩餘元素的串列 [2, 3]。
