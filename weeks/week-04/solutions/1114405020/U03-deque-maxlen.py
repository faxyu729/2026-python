# U3. deque(maxlen=N) 為何能保留最後 N 筆（1.3）
# deque 念作 "deck"，全名是 Double-Ended Queue (雙向佇列)。
# 它是 Python 標準函式庫 `collections` 內建的一種資料結構，
# 最大的特色是在頭尾兩端進行新增或刪除元素時，效能 (O(1)) 都比一般的串列 List (O(n)) 還要快非常多。

from collections import deque

# ---------------------------------------------------------
# maxlen 參數的作用：建立一個固定長度的「滑動視窗」
# ---------------------------------------------------------
# 當我們在建立 deque 時指定了 maxlen 參數 (這裡設為 3)
# 代表這個佇列的「最大容量」就是 3。
q = deque(maxlen=3)

# 接著我們透過一個 for 迴圈，依序把 1 到 5 推進這個佇列中。
for i in [1, 2, 3, 4, 5]:
    q.append(i)

    # 💡 運作過程拆解：
    # 迴圈第 1 次：加入 1 -> q 變成 [1]
    # 迴圈第 2 次：加入 2 -> q 變成 [1, 2]
    # 迴圈第 3 次：加入 3 -> q 變成 [1, 2, 3]  (此時佇列已滿)
    #
    # 迴圈第 4 次：加入 4 ->
    #   ⚠️ 因為 maxlen=3，容量滿了！
    #   deque 的預設行為會把「最舊的資料」自動擠出去！
    #   所以最左邊的 1 被踢掉，4 加在右邊。
    #   -> q 變成 [2, 3, 4]
    #
    # 迴圈第 5 次：加入 5 ->
    #   最左邊的 2 被踢掉，5 加在右邊。
    #   -> q 變成 [3, 4, 5]

# 最終結果：佇列裡面只會保留「最後推進去的 N 筆資料」。
# 這在做「只保留最近 5 筆錯誤紀錄」、「只顯示最後 10 行日誌 (類似 linux tail 指令)」、
# 或「計算移動平均線 (Moving Average)」時，超級實用，因為你完全不需要手動去刪除舊資料！
