# R16. 過濾：推導式 / generator / filter / compress（1.16）

# ---------------------------------------------------------
# 方法一：使用「串列推導式 (List Comprehension)」進行過濾
# 這是 Python 中最直觀且常用的過濾方式
# ---------------------------------------------------------
mylist = [1, 4, -5, 10]
# 這裡會產生一個全新的串列，裡面只包含 mylist 中大於 0 的數字
# 結果會是：[1, 4, 10]
positive_list = [n for n in mylist if n > 0]


# ---------------------------------------------------------
# 方法二：使用「產生器運算式 (Generator Expression)」進行過濾
# ---------------------------------------------------------
# 把中括號 [] 換成小括號 ()，就會變成產生器 (Generator)
# 產生器的特色是「不會一次把所有結果算出來放在記憶體」，而是「要用的時候才算」
# 對於資料量非常龐大的情況，可以大幅節省記憶體空間。
pos = (n for n in mylist if n > 0)
# 若要印出內容，通常需要用 list() 轉換或是用 for 迴圈遍歷
# print(list(pos))  # 結果：[1, 4, 10]


# ---------------------------------------------------------
# 方法三：使用內建函數 filter() 搭配自訂函式
# 當過濾邏輯比較複雜（例如需要使用 try-except 等無法寫在推導式裡的語法）時非常實用
# ---------------------------------------------------------
values = ["1", "2", "-3", "-", "N/A"]


def is_int(val):
    """
    判斷傳入的值是否可以被成功轉換為整數 (int)
    """
    try:
        int(val)  # 嘗試轉換為整數
        return True  # 如果沒噴錯，代表是合法的數字，回傳 True (保留此元素)
    except ValueError:
        return False  # 如果噴了 ValueError (例如遇到了 '-' 或 'N/A')，回傳 False (過濾掉此元素)


# filter 接收兩個參數：(判斷函式, 要過濾的序列)
# 它會把 values 裡的每個元素丟進 is_int 檢查，只有回傳 True 的會被留下來
# 因為 filter 回傳的是一個迭代器，所以外面包一層 list() 把它轉成串列
# 結果會是：['1', '2', '-3']
filtered_values = list(filter(is_int, values))


# ---------------------------------------------------------
# 方法四：使用 itertools.compress 透過布林值陣列進行過濾
# 當我們有兩個長度相同的陣列，一個是資料，另一個是判斷條件 (True/False) 時非常實用
# ---------------------------------------------------------
from itertools import compress

addresses = ["a1", "a2", "a3"]  # 原始資料陣列
counts = [0, 3, 10]  # 對應的數值陣列

# 先產生一個條件清單 (布林值陣列)，判斷 counts 裡的數字是否大於 5
# [0 > 5, 3 > 5, 10 > 5]  => [False, False, True]
more5 = [n > 5 for n in counts]

# compress 接收兩個參數：(資料陣列, 布林值陣列)
# 它會拿出布林值陣列中對應位置為 True 的資料
# 以這個例子來說，只有第三個是 True，所以只會留下 addresses 的第三個元素 'a3'
# 結果會是：['a3']
compressed_addresses = list(compress(addresses, more5))
