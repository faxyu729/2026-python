# U6. defaultdict 為何比手動初始化乾淨（1.6）
# 這個範例展示了處理「一鍵對多值 (One-to-Many)」字典時，
# 傳統字典與 defaultdict 在程式碼簡潔度上的巨大差異。

from collections import defaultdict

# 我們有一組配對資料，想要將它們整理成字典，同一個 key 要收集所有的 value。
# 預期的結果是 {'a': [1, 2], 'b': [3]}
pairs = [("a", 1), ("a", 2), ("b", 3)]

# ---------------------------------------------------------
# 寫法一：傳統手動版 (使用一般字典 dict)
# ---------------------------------------------------------
d = {}
for k, v in pairs:
    # 每次要加入資料前，都必須先檢查這個 key 是不是「第一次出現」
    if k not in d:
        # 如果是第一次出現，必須手動幫它建立一個空的串列 []，否則等一下 append 會報錯 (KeyError)
        d[k] = []

    # 確保裡面已經有串列後，才把數值塞進去
    d[k].append(v)


# ---------------------------------------------------------
# 寫法二：優雅版 (使用 collections.defaultdict)
# ---------------------------------------------------------
# 在建立 defaultdict 時，我們傳入一個「工廠函式 (Factory Function)」，這裡是 list。
# 意思是：「如果有人要存取一個不存在的 key，請自動呼叫 list() 產生一個空串列給它」。
d2 = defaultdict(list)

for k, v in pairs:
    # 這裡發生了魔法！
    # 當遇到 d2['a'] 第一次出現時，它不會噴 KeyError，
    # 而是自動在背後執行 d2['a'] = list() 建立空串列，然後立刻執行 .append(v)。
    # 完全省去了 if k not in d 的煩人判斷！
    d2[k].append(v)

# 💡 補充：
# 除了 list，你也可以傳入 set (自動建立空集合以防重複)、
# 或是 int (自動建立 0，非常適合用來做計數器：d[k] += 1)。
