# R17. 字典子集（1.17）
# 這個技巧展示如何從一個大字典中，篩選出符合條件的部分，並產生一個新的「子字典」。

# 假設這是我們擁有的原始字典，記錄了幾家公司的股票價格
prices = {"ACME": 45.23, "AAPL": 612.78, "IBM": 205.55}

# ---------------------------------------------------------
# 情境一：根據「值 (Value)」來篩選字典
# ---------------------------------------------------------
# 我們想要找出股價 (value) 大於 200 的公司。
# 這裡使用了「字典推導式 (Dictionary Comprehension)」的語法： {key: value for key, value in dict.items() if condition}
#
# 1. prices.items() 會把字典轉成 (key, value) 的組合，例如 ('ACME', 45.23)
# 2. if v > 200 會過濾掉價格小於等於 200 的項目
# 3. 最終產生新的字典 p1
#
# 結果會是：{'AAPL': 612.78, 'IBM': 205.55}
p1 = {k: v for k, v in prices.items() if v > 200}


# ---------------------------------------------------------
# 情境二：根據「鍵 (Key)」來篩選字典
# ---------------------------------------------------------
# 假設我們有一份特定的公司名單，我們只想取出這份名單內公司的股價。
# 這裡使用集合 (Set) 來儲存名單，因為集合在判斷元素是否存在 (in) 時，速度比串列 (List) 快非常多！
tech_names = {"AAPL", "IBM"}

# 同樣使用字典推導式：
# 這邊的條件是 `if k in tech_names`，意思是「如果這個公司的名字 (key) 有在我的科技公司名單裡，我才把它加進新的字典」。
#
# 結果會是：{'AAPL': 612.78, 'IBM': 205.55}
p2 = {k: v for k, v in prices.items() if k in tech_names}

