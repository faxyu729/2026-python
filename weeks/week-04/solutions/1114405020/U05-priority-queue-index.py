# U5. 優先佇列為何要加 index（1.5）
# 在自己手刻「優先佇列 (Priority Queue)」時，為什麼我們通常會在資料裡塞入一個「遞增的索引值 (index)」？
# 這個範例清楚展示了不加 index 會遇到的致命錯誤 (TypeError)，以及加了 index 是如何完美解決問題的。

import heapq


# 建立一個極簡單的自訂類別，用來代表我們要丟進佇列的「任務或資料」
class Item:
    def __init__(self, name):
        self.name = name

    # ⚠️ 注意：這個類別並沒有定義 __lt__ (小於) 之類的魔法方法。
    # 所以 Python 不知道如果遇到兩個 Item 物件，誰比誰大、誰比誰小。


pq = []

# ---------------------------------------------------------
# 錯誤示範：只放 (priority, item)
# ---------------------------------------------------------
# 如果我們把資料包成 Tuple：(優先權, 資料物件) 丟進 heap 裡面。
# 第一次 push 沒問題，因為佇列只有它一個人。
# heapq.heappush(pq, (-1, Item('a')))

# 當第二次 push 時，如果剛好「優先權」一樣 (都是 -1)！
# heapq.heappush(pq, (-1, Item('b')))  # 💥 這裡會發生 TypeError 錯誤！
#
# 💡 為什麼會噴錯？
# Python 在比較 Tuple 大小時，是從左比到右的。
# 當它發現兩個元素的優先權都是 -1 (分不出勝負) 時，它會被迫去比較 Tuple 的第二個元素，也就是 Item('a') 和 Item('b')。
# 但因為我們沒有教 Python 怎麼比較兩個 Item，所以程式當場崩潰。


# ---------------------------------------------------------
# 正確寫法：加入一個「遞增的 index」，變成 (priority, index, item)
# ---------------------------------------------------------
# 我們引入一個只增不減的計數器 `idx`，用來記錄這筆資料是「第幾個」加進來的。
idx = 0

# 現在資料結構變成了： (優先權, 順序, 資料物件)
heapq.heappush(pq, (-1, idx, Item("a")))
idx += 1
heapq.heappush(pq, (-1, idx, Item("b")))
idx += 1

# 💡 為什麼這樣就不會噴錯了？
# 1. 兩者優先權都是 -1，平手，往下一關比。
# 2. 第二關比的是 `idx`！因為 `idx` 每次都會 +1，所以絕對不可能重複！
# 3. Item('a') 的 idx 是 0，Item('b') 的 idx 是 1。
# 4. 0 < 1，所以 Python 立刻判定 Item('a') 贏了，順序就決定好了！
# 5. 因為在第二關就分出勝負了，Python 【根本不需要，也不會去比較】第三個元素 (Item 物件)。
#
# 附帶好處：這樣寫不僅解決了報錯，還順便實作了「穩定排序 (Stable Sort)」的特性，
# 也就是當優先權相同時，「先加入的任務，會先被執行」！
