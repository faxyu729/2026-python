"""
UVA 10812 - Beat the Spread! 完整解決方案 - 詳細中文註解版

題目名稱：Beat the Spread!
平台：UVA Online Judge
問題編號：10812

=============================================================================
題目說明
=============================================================================

超級盃賭局中：
- 有人押注兩隊分數的和 S
- 有人押注兩隊分數的差 D（絕對值）

給定和 S 與差 D，求兩隊各自的得分（較大的先輸出）。
若無整數解或出現負數得分，則輸出 impossible。

=============================================================================
輸入說明
=============================================================================

- 第一行為測試資料組數 n
- 每組測試資料一行，包含兩個非負整數 S（和）與 D（差）

範例：
2
40 20
20 40

=============================================================================
輸出說明
=============================================================================

每組測試資料輸出一行：
- 若有解：輸出兩個整數（較大的先），以空格分隔
- 若無解：輸出 impossible

範例輸出：
30 10
impossible

=============================================================================
解題思路
=============================================================================

★ 第 1 步：建立方程式

設兩隊得分分別為 a 和 b（a >= b，a 是較高分）

根據題意，我們有：
  - 方程式 1：a + b = S（分數之和）
  - 方程式 2：a - b = D（分數之差，因為 a >= b）

★ 第 2 步：聯立方程式求解

方程式 1 + 方程式 2：
  (a + b) + (a - b) = S + D
  2a = S + D
  a = (S + D) / 2  ← 較高分

方程式 1 - 方程式 2：
  (a + b) - (a - b) = S - D
  2b = S - D
  b = (S - D) / 2  ← 較低分

★ 第 3 步：驗證解的有效性

需要檢查以下條件，否則無解（impossible）：

1. (S + D) 必須是偶數
   原因：a = (S + D) / 2 必須是整數
   
2. (S - D) 必須是偶數
   原因：b = (S - D) / 2 必須是整數
   
3. b >= 0（較低分不能為負數）
   原因：得分不能是負數
   檢查方式：S >= D（因為 b = (S - D) / 2 >= 0 ⟺ S >= D）

★ 第 4 步：輸出結果

如果通過所有驗證，輸出 a 和 b（較大的先）

=============================================================================
驗證範例
=============================================================================

例 1：S = 40, D = 20
  a = (40 + 20) / 2 = 60 / 2 = 30 ✓
  b = (40 - 20) / 2 = 20 / 2 = 10 ✓
  驗證：30 + 10 = 40 ✓，30 - 10 = 20 ✓
  輸出：30 10

例 2：S = 20, D = 40
  a = (20 + 40) / 2 = 60 / 2 = 30 ✓
  b = (20 - 40) / 2 = -20 / 2 = -10 ✗（負分！）
  輸出：impossible

例 3：S = 5, D = 2
  S + D = 7（奇數）
  a = 7 / 2 = 3.5 ✗（不是整數）
  輸出：impossible

例 4：S = 50, D = 50
  a = (50 + 50) / 2 = 50 ✓
  b = (50 - 50) / 2 = 0 ✓
  驗證：50 + 0 = 50 ✓，50 - 0 = 50 ✓
  輸出：50 0

=============================================================================
演算法時間和空間複雜度
=============================================================================

時間複雜度：O(1)
  - 只進行固定次數的算術運算
  - 不管輸入有多大，運算次數都是常數

空間複雜度：O(1)
  - 只使用常數個變數（s, d, higher_score, lower_score）
  - 不隨著輸入大小增長

=============================================================================
實現代碼
=============================================================================
"""


def solve_q10812():
    """
    解題主程式
    
    功能：
    1. 讀取測試組數
    2. 對每組測試資料進行計算
    3. 輸出結果或 impossible
    
    核心公式：
    - 較高分 a = (S + D) / 2
    - 較低分 b = (S - D) / 2
    
    驗證條件：
    - (S + D) 必須是偶數
    - (S - D) 必須是偶數
    - b >= 0（即 S >= D）
    """
    
    # ===== 步驟 1：讀取測試組數 =====
    # 第一行輸入是一個整數 n，表示有 n 組測試資料
    n = int(input())
    
    # ===== 步驟 2：逐組處理測試資料 =====
    for _ in range(n):
        # 讀入和 S 與差 D
        # 使用 map(int, input().split()) 將輸入行分割成整數
        s, d = map(int, input().split())
        
        # ===== 步驟 3-1：檢查第一個驗證條件 =====
        # 檢查 (S + D) 是否為偶數
        # 為什麼？因為 a = (S + D) / 2 必須是整數
        # 如果 (S + D) 是奇數，則 (S + D) / 2 會是小數，無法得到整數解
        if (s + d) % 2 != 0:
            # (S + D) 是奇數，無法得到整數解
            print("impossible")
            continue  # 跳過本組，處理下一組
        
        # ===== 步驟 3-2：檢查第二個驗證條件 =====
        # 檢查 (S - D) 是否為偶數
        # 為什麼？因為 b = (S - D) / 2 必須是整數
        if (s - d) % 2 != 0:
            # (S - D) 是奇數，無法得到整數解
            print("impossible")
            continue
        
        # ===== 步驟 4：計算較高分和較低分 =====
        # 使用整除運算符 // 確保得到整數
        # (S + D) / 2 計算較高分
        higher_score = (s + d) // 2
        
        # (S - D) / 2 計算較低分
        lower_score = (s - d) // 2
        
        # ===== 步驟 5：檢查第三個驗證條件 =====
        # 檢查較低分是否為非負
        # 為什麼？因為得分不能是負數
        if lower_score < 0:
            # 較低分為負，無有效解
            print("impossible")
        else:
            # ===== 步驟 6：輸出結果 =====
            # 輸出較大的分先，再輸出較低的分
            # 使用 f-string 格式化輸出
            print(f"{higher_score} {lower_score}")


# ===== 補充說明：為什麼要檢查偶數性 =====
# 
# 在整數運算中，如果我們要計算 n/2 並要求結果是整數，
# 那麼 n 必須是偶數。
#
# 例如：
#   5 / 2 = 2.5（不是整數）✗
#   6 / 2 = 3.0（是整數）✓
#
# 在 Python 中：
#   5 % 2 == 1（奇數）
#   6 % 2 == 0（偶數）
#
# 所以我們用 (n % 2 != 0) 來檢查 n 是否為奇數


# ===== 補充說明：為什麼要檢查非負 =====
#
# 在超級盃的情境中，得分必須是非負的。
# 如果計算出來的較低分是負數，這就不符合現實，所以無解。
#
# 例如：S=20, D=40
#   a = (20+40)/2 = 30
#   b = (20-40)/2 = -10 ✗（負分！）
#
# 這種情況下，即使數學上可以解方程式，
# 但在實際問題中是無意義的，所以輸出 impossible。


if __name__ == "__main__":
    # 程式進入點
    solve_q10812()
