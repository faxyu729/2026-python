#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
UVA 10170 - 無限房間旅館 標準解答

=== 問題分析 ===
旅館中的旅行團按規則住宿：
- 第 i 個旅行團有 (S + i - 1) 人
- 第 i 個旅行團住 (S + i - 1) 天
- 旅行團按順序住宿，無重疊

給定起始人數 S 和查詢天數 D，求第 D 天的旅行團人數。

=== 演算法設計 ===

1. 問題分析
   ===============================================================
   設第 i 個旅行團（i=1, 2, 3, ...）有 n_i = S + i - 1 人

   第 i 個旅行團的：
   - 人數：n_i = S + i - 1
   - 住宿天數：n_i 天
   - 開始天數：sum(n_1 + n_2 + ... + n_{i-1}) + 1
   - 結束天數：sum(n_1 + n_2 + ... + n_i)

   需要找到最小的 i 使得：
   sum(n_1 + n_2 + ... + n_i) >= D

2. 數學推導
   ===============================================================
   設 n = S + i - 1（第 i 個團隊的人數）

   前 i 個團隊住宿的總天數：
   sum = S + (S+1) + (S+2) + ... + (S+i-1)
       = i*S + (0+1+2+...+(i-1))
       = i*S + i(i-1)/2
       = i(S + (i-1)/2)
       = i(2S + i - 1)/2

   需要找到最小的 i 使得：
   i(2S + i - 1)/2 >= D
   i(2S + i - 1) >= 2D
   i² + (2S-1)i - 2D >= 0

   使用二次公式：
   i = (-(2S-1) + sqrt((2S-1)² + 8D)) / 2

3. 演算法流程
   ===============================================================
   a. 使用二次公式計算 i（團隊編號）
   b. 驗證 i 是否正確（是否滿足條件）
   c. 返回第 i 個團隊的人數：S + i - 1

4. 時間複雜度分析
   ===============================================================
   - 二次公式計算：O(1)
   - 驗證：O(1)

   總時間複雜度：O(1)

   這是最優的，因為 D 最大為 10^15，不能通過逐項遍歷。

5. 空間複雜度
   ===============================================================
   O(1)：僅使用常數個變數

=== 實現細節 ===

1. 輔助函數
   ===============================================================
   - sum_of_teams(s, i): 計算前 i 個團隊的總住宿天數
   - find_group_size(s, d): 主求解函數

2. 邊界處理
   ===============================================================
   - 確保 D >= 1（題目保證）
   - 確保 S >= 1（題目保證）
   - 使用浮點數計算後轉換為整數

3. 驗證邏輯
   ===============================================================
   計算出的 i 可能需要調整，因為浮點數計算的精度問題
   需要檢查：
   - sum_of_teams(s, i) >= D
   - sum_of_teams(s, i-1) < D（確保 i 是最小的）

=== 測試驗證 ===

已設計的測試用例包括：
1. 基本情況：第一個到第三個團隊
2. 邊界情況：S=1, S=10000
3. 數學性質：單調性、一致性、界限
4. 大數值：D 接近 10^15

"""

import math
from typing import Tuple


def sum_of_teams(s: int, i: int) -> int:
    """
    計算前 i 個旅行團的總住宿天數。

    第 k 個旅行團有 (S + k - 1) 人，住 (S + k - 1) 天。

    前 i 個團隊的總天數：
    sum = S + (S+1) + (S+2) + ... + (S+i-1)
        = i*S + (0+1+2+...+(i-1))
        = i*S + i(i-1)/2
        = i(2S + i - 1)/2

    時間複雜度：O(1)

    參數：
        s: 第一個旅行團的人數
        i: 旅行團的序號（第 i 個團隊）

    返回：
        前 i 個旅行團的總住宿天數

    例子：
        sum_of_teams(4, 1) → 4      (第 1 個團隊，4 人，住 4 天)
        sum_of_teams(4, 2) → 9      (前 2 個團隊，4+5=9 天)
        sum_of_teams(4, 3) → 15     (前 3 個團隊，4+5+6=15 天)
    """
    # 使用公式計算：i(2S + i - 1) / 2
    return i * (2 * s + i - 1) // 2


def find_group_size_binary_search(s: int, d: int) -> int:
    """
    使用二分查找法找到第 D 天的旅行團人數。

    演算法：
    1. 設置左邊界 left = s，右邊界 right = s + 某個大值
    2. 使用二分查找找到最小的 i 使得 sum_of_teams(s, i) >= d
    3. 返回第 i 個團隊的人數：s + i - 1

    時間複雜度：O(log(d)) ≈ O(50)
    空間複雜度：O(1)

    參數：
        s: 第一個旅行團的人數
        d: 查詢的天數

    返回：
        第 d 天的旅行團人數

    例子：
        find_group_size_binary_search(4, 5) → 5
        find_group_size_binary_search(4, 15) → 6
    """
    # 確定搜索範圍
    # 最壞情況下，第 D 天的團隊人數最多為 D
    # 但為了效率，我們可以用數學估計上界

    left = 1
    right = s + int(math.sqrt(2 * d)) + 10  # 估計上界

    while left < right:
        mid = (left + right) // 2

        if sum_of_teams(s, mid) < d:
            # 第 mid 個團隊不夠，往右搜
            left = mid + 1
        else:
            # 第 mid 個團隊足夠，往左搜
            right = mid

    # left 就是最小的 i 使得 sum_of_teams(s, i) >= d
    return s + left - 1


def find_group_size_quadratic(s: int, d: int) -> int:
    """
    使用二次公式法找到第 D 天的旅行團人數。

    演算法：
    1. 使用二次公式求解 i
    2. 驗證並調整 i
    3. 返回第 i 個團隊的人數

    推導：
    需要找最小的 i 使得：
    i(2S + i - 1)/2 >= D
    i(2S + i - 1) >= 2D
    i² + (2S-1)i - 2D >= 0

    由二次公式：
    i = (-(2S-1) + sqrt((2S-1)² + 8D)) / 2

    時間複雜度：O(1)
    空間複雜度：O(1)

    參數：
        s: 第一個旅行團的人數
        d: 查詢的天數

    返回：
        第 d 天的旅行團人數
    """
    # 計算判別式
    # 方程：i² + (2S-1)i - 2D = 0
    a = 1
    b = 2 * s - 1
    c = -2 * d

    discriminant = b * b - 4 * a * c

    # 求解
    i = (-b + math.sqrt(discriminant)) / (2 * a)

    # 四捨五入到整數
    i = int(math.ceil(i))

    # 驗證並調整
    # 檢查 i 和 i-1，取最小的滿足條件的
    while i > 1 and sum_of_teams(s, i - 1) >= d:
        i -= 1

    # 檢查 i 是否滿足條件
    if sum_of_teams(s, i) < d:
        i += 1

    # 返回第 i 個團隊的人數
    return s + i - 1


def find_group_size(s: int, d: int) -> int:
    """
    主求解函數：找到第 D 天的旅行團人數。

    使用二分查找法（更穩定），避免浮點數精度問題。

    演算法邏輯：
    1. 列舉旅行團序號 i = 1, 2, 3, ...
    2. 計算前 i 個旅行團的總住宿天數
    3. 找到最小的 i 使得這個和 >= D
    4. 返回第 i 個旅行團的人數：S + i - 1

    時間複雜度：O(log(d))
    空間複雜度：O(1)

    參數：
        s: 第一個旅行團的人數（1 ≤ s ≤ 10000）
        d: 查詢的天數（1 ≤ d < 10^15）

    返回：
        第 d 天的旅行團人數

    例子：
        find_group_size(4, 1) → 4    (第 1 天，第 1 個團隊，4 人)
        find_group_size(4, 4) → 4    (第 4 天，第 1 個團隊，4 人)
        find_group_size(4, 5) → 5    (第 5 天，第 2 個團隊，5 人)
        find_group_size(4, 9) → 5    (第 9 天，第 2 個團隊，5 人)
        find_group_size(4, 10) → 6   (第 10 天，第 3 個團隊，6 人)
    """
    # 使用二分查找法
    return find_group_size_binary_search(s, d)


def main():
    """
    主程序：讀取輸入並調用求解函數。

    輸入格式：每行一對 (S, D)，直到 EOF
    輸出格式：每行一個整數，表示第 D 天的旅行團人數
    """
    import sys

    for line in sys.stdin:
        parts = line.strip().split()
        if len(parts) >= 2:
            s = int(parts[0])
            d = int(parts[1])
            result = find_group_size(s, d)
            print(result)


if __name__ == "__main__":
    main()
