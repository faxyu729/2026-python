"""
題目 10062 - 乳牛排隊問題

==============================================================================
                            問題描述
==============================================================================

背景故事：
有N頭乳牛（編號1到N）在排隊吃晚飯。牠們喝了太多啤酒，排列順序亂了。
農夫FJ無法直接看出牠們的編號，但他記錄了一個有用的數據：
  "對於隊伍中的每頭乳牛，他知道在該乳牛前面、編號比它小的乳牛有幾頭"

任務：根據這份數據重建正確的排列順序

==============================================================================
                        輸入輸出格式
==============================================================================

輸入：
  第1行：N（乳牛總數，2 ≤ N ≤ 80,000）
  第2至N行：共N-1行
    - 第2行：第2個位置的乳牛前面有多少頭編號<它的乳牛
    - 第3行：第3個位置的乳牛前面有多少頭編號<它的乳牛
    - ...
    - 第N行：第N個位置的乳牛前面有多少頭編號<它的乳牛
  （第1個位置沒有輸入，因為前面沒有乳牛）

輸出：N行，每行一個數字（1到N的排列）

==============================================================================
                        範例
==============================================================================

輸入：
  4
  1
  2
  3

解釋：
  位置1：編號？（無限制）
  位置2：前面有1頭編號<它的牛
  位置3：前面有2頭編號<它的牛
  位置4：前面有3頭編號<它的牛

  答案：[1, 2, 3, 4]
  驗證：
    位置1牛1：前面0頭<1 ✓
    位置2牛2：前面1頭<2(牛1) ✓
    位置3牛3：前面2頭<3(牛1,2) ✓
    位置4牛4：前面3頭<4(牛1,2,3) ✓

==============================================================================
                        解題思路
==============================================================================

核心觀察：
  每個位置都有一個約束條件（除了位置1）。我們需要找到一個排列，
  使得每個位置的乳牛都滿足其約束條件。

方法1（回溯法）- 本解法採用：
  思路：
    依序為每個位置選擇乳牛編號，確保滿足約束。
    如果某個選擇導致後續無法滿足約束，就回溯並嘗試其他選擇。

  實現：
    • 使用遞歸函數 solve_recursive(pos, result, used)
    • pos：當前要填充的位置
    • result：已填充位置的編號列表
    • used：已使用編號的標記

  時間複雜度：
    • 最壞O(N! × N)，但實際情況遠優於此
    • 約束條件大幅剪枝了搜索空間

  空間複雜度：O(N)用於遞歸棧和已使用標記

方法2（Fenwick樹/二分搜索） - 高效但複雜：
  思路：
    使用Fenwick樹追蹤已使用的位置，然後二分搜索找到正確位置。
    時間複雜度O(N log² N)
    但實現較複雜，本題回溯法已夠高效。

==============================================================================
"""


def solve_cow_problem(input_string):
    """
    解決乳牛排隊問題

    參數：
        input_string (str)：輸入字符串，包含N和每個位置前面編號較小的牛數

    返回：
        list：重建後的乳牛排列順序（長度為N的列表，包含1到N的編號）

    演算法概述（回溯法）：
    1. 解析輸入，提取N和約束條件數組info
    2. 定義can_place(pos, candidate, result)檢查可行性
    3. 定義solve_recursive進行回溯搜索
    4. 返回找到的排列或None
    """
    # ========================================================================
    # 步驟1：解析輸入
    # ========================================================================
    lines = input_string.strip().split("\n")
    n = int(lines[0])  # 乳牛總數

    # 讀取約束信息
    # info[i-2] = 第i個位置的乳牛前面應該有多少頭編號<它的乳牛（i >= 2）
    # 例如：
    #   info[0] = 第2個位置的約束
    #   info[1] = 第3個位置的約束
    #   ...
    #   info[n-2] = 第n個位置的約束
    info = []
    for i in range(1, n):
        info.append(int(lines[i]))

    # ========================================================================
    # 步驟2：定義輔助函數can_place
    # ========================================================================
    def can_place(pos, candidate, result):
        """
        檢查編號candidate是否可以放在位置pos

        參數：
            pos (int)：要檢查的位置（1到n）
            candidate (int)：要放置的乳牛編號（1到n）
            result (list)：已填充位置的編號列表（按順序）

        返回：
            bool：True表示candidate可以放在pos，False表示不能

        邏輯：
            • 位置1：沒有約束，總是返回True
            • 位置i(i>=2)：
              - 計算result中編號<candidate的乳牛個數
              - 檢查該個數是否等於info[i-2]（該位置的約束）
              - 相等則返回True，否則False

        例子：
            如果pos=3, candidate=2, result=[4,1]
            則smaller_count = 1（只有1<2）
            檢查info[3-2]=info[1]是否等於1
        """
        if pos == 1:
            # 位置1沒有約束條件
            return True

        # 計算result中編號<candidate的乳牛個數
        target_smaller = info[pos - 2]  # 該位置的約束
        smaller_count = sum(1 for x in result if x < candidate)

        # 檢查實際個數是否等於約束
        return smaller_count == target_smaller

    # ========================================================================
    # 步驟3：定義遞歸回溯函數
    # ========================================================================
    def solve_recursive(pos, result, used):
        """
        使用回溯法遞歸求解

        參數：
            pos (int)：當前要填充的位置（1到n）
            result (list)：已確定的排列（長度為pos-1）
            used (list)：已使用編號的標記（used[i]=True表示編號i已使用）

        返回：
            list：找到的完整排列（長度為n）
            None：如果無法找到有效排列（通常不發生，因為題目保證有解）

        工作流程：
            1. 基礎情況（Base Case）：
               如果pos > n，說明已填充所有位置，返回結果

            2. 遞歸情況（Recursive Case）：
               對於每個未使用的編號候選
                 a. 檢查該編號是否可放在pos（調用can_place）
                 b. 如果可以：
                    • 標記為已使用
                    • 加入result
                    • 遞歸求解下一位置
                    • 如果遞歸成功，返回結果
                    • 否則回溯：移除標記和result
                 c. 如果所有候選都失敗，返回None表示此路不通

        回溯的直觀理解：
            想像在迷宮中探路：
            • 每個位置尋找可行的編號（like choosing a direction）
            • 試著繼續下去（遞歸）
            • 如果走到死胡同，退回並嘗試其他編號（回溯）
            • 直到找到出口（完整排列）

        複雜度分析：
            • 最壞情況：嘗試所有N!種排列，每次O(N)檢查 → O(N!×N)
            • 實際情況遠優於此（約束剪枝）
            • 空間：O(N)遞歸棧深度 + O(N)存儲result和used
        """
        # ====== 基礎情況：已填充所有位置 ======
        if pos == n + 1:
            # 所有n個位置都已填充，找到完整排列
            # 返回result的副本（以防後續修改影響結果）
            return result[:]

        # ====== 遞歸情況：嘗試為當前位置填入編號 ======
        # 嘗試編號1到n
        for candidate in range(1, n + 1):
            # 跳過已使用的編號
            if used[candidate]:
                continue

            # 檢查該編號是否可以放在當前位置
            if can_place(pos, candidate, result):
                # ====== 選擇該編號 ======
                # 標記為已使用
                used[candidate] = True
                # 加入排列
                result.append(candidate)

                # ====== 遞歸求解下一個位置 ======
                solution = solve_recursive(pos + 1, result, used)

                # 如果遞歸找到完整排列，返回
                if solution is not None:
                    return solution

                # ====== 回溯：撤銷選擇 ======
                # 移除剛加入的編號
                result.pop()
                # 標記為未使用
                used[candidate] = False
                # 迴圈繼續，嘗試下一個編號

        # ====== 所有編號都嘗試失敗 ======
        # 無法為當前位置找到合適的編號
        # 返回None表示此路不通，調用者將回溯
        return None

    # ========================================================================
    # 步驟4：啟動遞歸求解
    # ========================================================================
    # 從位置1開始，空排列，所有編號未使用
    return solve_recursive(1, [], [False] * (n + 1))


# ==============================================================================
#                            主程序
# ==============================================================================


def main():
    """
    主程序：從標準輸入讀取測試數據並輸出結果
    """
    import sys

    # 讀取整個輸入
    input_string = sys.stdin.read()

    # 求解
    result = solve_cow_problem(input_string)

    # 輸出結果
    if result:
        # 每個編號一行
        for cow_id in result:
            print(cow_id)
    else:
        # 無解（通常不應該發生，題目保證有解）
        print("無解")


if __name__ == "__main__":
    # 檢查是否有輸入
    import sys

    if not sys.stdin.isatty():
        main()
