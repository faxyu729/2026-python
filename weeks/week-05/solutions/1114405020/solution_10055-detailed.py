"""
UVA 10055 - 複合函數的單調性判斷（詳細註解版本）

====================================================================
                         問題完整描述
====================================================================

數學背景:
--------
在微積分和函數論中，我們常常研究函數的單調性（monotonicity）。
一個函數可以是：
• 單調遞增（增函數）：若 x1 < x2，則 f(x1) < f(x2)
• 單調遞減（減函數）：若 x1 < x2，則 f(x1) > f(x2)

複合函數的單調性：
-----------------
當我們將函數進行複合時，結果函數的單調性取決於組成它的各個函數。

具體規則：
---------
設 f 和 g 是兩個函數，考慮複合函數 h(x) = f(g(x))

1. 如果 f 和 g 都是增函數：
   h 是增函數

2. 如果 f 和 g 都是減函數：
   h 是增函數（「負負得正」）

3. 如果 f 和 g 一個增一個減：
   h 是減函數

問題設定:
--------
給定 N 個函數 f₁, f₂, ..., fₙ，初始全為增函數。
可以執行兩種操作：
1. 反轉：改變某個函數的增減性
2. 查詢：詢問 F(x) = f_L(f_{L+1}(...f_R(x)...)) 的增減性

注意：複合的順序很重要！
F(x) = f_L(f_{L+1}(...f_R(x)...)) 表示先執行 f_R，再執行 f_{R-1}，...，最後執行 f_L

====================================================================
                         演算法核心
====================================================================

關鍵觀察:
--------
複合函數的單調性只取決於「減函數的個數」，而與具體的函數或它們的
相對位置無關。

證明邏輯:
--------
考慮複合 F(x) = f_L(f_{L+1}(...f_R(x)...))

令 g(x) = f_{L+1}(...f_R(x)...)（後面的複合）

如果 f_L 是增函數：
  F 的單調性 = g 的單調性

如果 f_L 是減函數：
  F 的單調性 = g 的相反單調性

因此，每增加一個減函數，結果的單調性就反轉一次。

最終結論:
--------
減函數個數為奇數時，複合函數是減函數
減函數個數為偶數時，複合函數是增函數

可以用模運算表示：
  result = (減函數個數) % 2
  - 0 表示增函數
  - 1 表示減函數

====================================================================
                         具體例子
====================================================================

例子 1: 兩個函數，都是增函數
-------
f₁(x) = x + 1（增函數）
f₂(x) = 2x（增函數）
F(x) = f₁(f₂(x)) = f₁(2x) = 2x + 1（增函數）
減函數個數: 0（偶數）→ 結果是增函數 ✓

例子 2: 兩個函數，一個增一個減
-------
f₁(x) = -x（減函數）
f₂(x) = 2x（增函數）
F(x) = f₁(f₂(x)) = f₁(2x) = -2x（減函數）
減函數個數: 1（奇數）→ 結果是減函數 ✓

例子 3: 三個函數，兩個減函數
-------
f₁(x) = -x（減函數）
f₂(x) = 2x（增函數）
f₃(x) = -x（減函數）
F(x) = f₁(f₂(f₃(x)))
     = f₁(f₂(-x))
     = f₁(-2x)
     = -(-2x) = 2x（增函數）
減函數個數: 2（偶數）→ 結果是增函數 ✓

====================================================================
                         程式設計思路
====================================================================

資料結構:
--------
使用一個陣列存儲 N 個函數的增減性：
  functions[i] = 0 表示 f_{i+1} 是增函數
  functions[i] = 1 表示 f_{i+1} 是減函數

操作 1 - 反轉（Toggle）:
------------------------
當需要反轉第 i 個函數時：
  functions[i-1] = 1 - functions[i-1]

如果是 0，變成 1；如果是 1，變成 0。
時間複雜度: O(1)

操作 2 - 查詢（Query）:
----------------------
給定範圍 [L, R]，計算減函數個數：
  count = 0
  for i in [L, R]:
      if functions[i-1] == 1:
          count += 1

然後返回 count % 2
時間複雜度: O(R - L + 1)

總時間複雜度: O(N + Q × (R - L))
在最壞情況下是 O(N + Q × N)

====================================================================
                         邊界情況討論
====================================================================

1. 查詢單個函數 (L == R):
   直接返回 functions[L-1]

2. 查詢全部函數 (L = 1, R = N):
   計算所有減函數的個數

3. 多次反轉同一函數:
   第一次反轉：0 → 1
   第二次反轉：1 → 0（恢復原樣）

4. 沒有減函數:
   所有函數都是增函數，複合結果是增函數

====================================================================
"""


class MonotonFunction:
    """
    複合函數單調性判斷類（詳細版本）

    用於管理多個函數的增減性，並快速判斷其複合函數的單調性。
    """

    def __init__(self, n):
        """
        初始化 N 個函數

        參數:
        ----
        n: int
            函數個數 (1 ≤ N ≤ 200,000)

        說明:
        ----
        所有函數初始為增函數，用 0 表示，1 表示減函數。
        使用 0-based 陣列索引存儲，但函數編號是 1-based。
        """
        # 初始化所有函數為增函數
        # self.functions[i] 表示 f_{i+1} 的增減性
        self.functions = [0] * n

        # 保存函數個數
        self.n = n

    def toggle(self, i):
        """
        反轉第 i 個函數的增減性

        參數:
        ----
        i: int
            要反轉的函數編號（1-based，範圍：1 到 N）

        說明:
        ----
        將 f_i 的增減性反轉：
        • 若 f_i 是增函數 (0)，變成減函數 (1)
        • 若 f_i 是減函數 (1)，變成增函數 (0)

        實現方式：
        --------
        使用 XOR 運算的簡潔方式：
        functions[i-1] = 1 - functions[i-1]

        例如：
        • functions[i-1] = 0 時，1 - 0 = 1
        • functions[i-1] = 1 時，1 - 1 = 0

        時間複雜度: O(1)
        """
        # 轉換為 0-based 索引
        idx = i - 1

        # 反轉增減性：0 ↔ 1
        self.functions[idx] = 1 - self.functions[idx]

    def query(self, l, r):
        """
        查詢複合函數 F(x) = f_L(f_{L+1}(...f_R(x)...)) 的單調性

        參數:
        ----
        l: int
            複合函數的左邊界（最外層函數編號，1-based）
        r: int
            複合函數的右邊界（最內層函數編號，1-based）

        返回值:
        ------
        int
            複合函數的單調性
            • 返回 0：F(x) 是增函數
            • 返回 1：F(x) 是減函數

        演算法說明:
        ----------
        步驟 1: 統計範圍 [L, R] 內減函數的個數

        步驟 2: 根據減函數個數判斷複合函數的單調性
               減函數個數 ≡ 0 (mod 2) → 增函數 → 返回 0
               減函數個數 ≡ 1 (mod 2) → 減函數 → 返回 1

        數學原理:
        --------
        複合運算可以看作是一系列的變換。每加入一個減函數，
        結果的單調性就反轉一次。

        減函數個數 (mod 2) 直接決定了最終的結果：
        • 偶數次反轉 → 回到原始狀態 (增) → 返回 0
        • 奇數次反轉 → 反轉一次 (減) → 返回 1

        時間複雜度: O(R - L + 1)
        在最壞情況下是 O(N)

        空間複雜度: O(1)（不使用額外空間）
        """

        # ============================================================
        # 步驟 1: 計算減函數的個數
        # ============================================================
        # 初始化減函數計數
        decreasing_count = 0

        # 遍歷範圍 [L, R] 內的所有函數
        # 注意：函數編號是 1-based（L 到 R）
        for i in range(l, r + 1):
            # 轉換為 0-based 陣列索引
            idx = i - 1

            # 如果 f_i 是減函數，計數增加 1
            if self.functions[idx] == 1:
                decreasing_count += 1

        # ============================================================
        # 步驟 2: 根據減函數個數的奇偶性判斷結果
        # ============================================================
        # 使用模運算判斷奇偶性
        # decreasing_count % 2：
        # • 如果是 0（偶數）→ 複合函數是增函數 → 返回 0
        # • 如果是 1（奇數）→ 複合函數是減函數 → 返回 1

        return decreasing_count % 2


# ====================================================================
# 輸入/輸出處理
# ====================================================================


def read_and_process_input():
    """
    從標準輸入讀取並處理測試資料

    輸入格式說明:
    -----------
    第一行: 兩個整數 N 和 Q
            • N: 函數個數 (1 ≤ N ≤ 200,000)
            • Q: 操作總數 (1 ≤ Q ≤ 200,000)

    接下來的 Q 行: 每行一個操作
            • 若為 1：反轉操作
              格式: 1 i
              表示將 f_i 的增減性反轉

            • 若為 2：查詢操作
              格式: 2 L R
              表示查詢 F(x) = f_L(f_{L+1}(...f_R(x)...)) 的增減性

    輸出格式說明:
    -----------
    對每個查詢操作（v = 2），輸出一行：
    • 0 表示 F(x) 是增函數
    • 1 表示 F(x) 是減函數

    範例:
    ----
    輸入:
    2 5
    1 1
    2 1 2
    1 2
    2 1 2
    2 1 1

    輸出:
    1
    0
    1

    解釋:
    • N=2，Q=5
    • 反轉 f1: f1 變成減函數
    • 查詢 f1(f2(x)): 1個減函數 → 奇數 → 輸出 1
    • 反轉 f2: f2 變成減函數
    • 查詢 f1(f2(x)): 2個減函數 → 偶數 → 輸出 0
    • 查詢 f1(x): 1個減函數 → 奇數 → 輸出 1
    """

    # 讀取 N 和 Q
    n, q = map(int, input().split())

    # 建立函數管理器
    mf = MonotonFunction(n)

    # 處理每個操作
    for _ in range(q):
        # 讀取一行操作
        operation = list(map(int, input().split()))
        v = operation[0]  # 操作類型

        if v == 1:
            # 反轉操作
            i = operation[1]
            mf.toggle(i)
        else:  # v == 2
            # 查詢操作
            l = operation[1]
            r = operation[2]
            result = mf.query(l, r)
            print(result)


