# 輸入/輸出處理
# ====================================================================


def read_and_process_input():
    """
    從標準輸入讀取並處理測試資料

    輸入格式:
    --------
    第一行: T (測試資料組數)

    對於每組測試資料:
        第一行: N (模擬天數，7 ≤ N ≤ 3650)
        第二行: P (政黨數量，1 ≤ P ≤ 100)
        接下來P行: 每行一個整數 h (罷會參數)

    輸出:
    ----
    對每組測試資料，輸出一個整數（損失的工作天數）

    範例:
    ----
    輸入:
    1
    14
    3
    3
    4
    8

    輸出:
    5
    """

    # 讀取測試資料組數
    t = int(input())

    # 處理每一組測試資料
    for _ in range(t):
        # 讀取模擬天數
        n = int(input())

        # 讀取政黨數量
        p = int(input())

        # 讀取每個政黨的罷會參數
        hartal_params = []
        for _ in range(p):
            h = int(input())
            hartal_params.append(h)

        # 計算並輸出結果
        result = calculate_hartal_days(n, hartal_params)
        print(result)


