"""
UVA 10041 - 尋找最小距離總和（詳細註解版本）

====================================================================
                         問題完整描述
====================================================================

故事背景:
--------
黑手黨老大 Vito Deadstone 要搬到紐約來，他有很多親戚都住在
Lamafia 大道上。他需要找一間房子，使得從他的新家到所有親戚
房子的距離總和最小。

問題的數學本質:
--------------
給定一組點在一維直線上的位置，找一個點使得到所有給定點的
距離總和最小。這是一個經典的統計學問題。

重要結論（中位數定理）:
---------------------
使距離總和最小的位置就是這些點的「中位數」（Median）。

為什麼是中位數?
--------------
• 對於奇數個點：中位數就是排序後的中間值
  例如: [1, 3, 5] 中位數是 3

• 對於偶數個點：最優位置在兩個中間值之間的任何位置
  例如: [1, 2, 3, 4] 最優位置在 2 和 3 之間
  這裡我們選擇 len(list)//2 位置的值，也能得到最優解

數學證明簡述:
-----------
如果位置偏離中位數向左移動，左邊點增加的距離總和會大於
右邊點減少的距離總和。同樣，向右移動也會增加總距離。
只有在中位數處，才能達到平衡。

====================================================================
                         演算法詳解
====================================================================

演算法步驟:
---------
1️⃣  排序：將親戚的位置從小到大排序
    原因：為了快速找到中位數

2️⃣  找中位數：取排序後陣列的中間位置
    方法：index = len(relatives) // 2
    對於長度為 n 的陣列：
    - n=1: index=0 (位置0是中位數)
    - n=2: index=1 (位置1在2個中間值的右邊)
    - n=3: index=1 (位置1是真正的中位數)
    - n=4: index=2 (位置2在2個中間值的右邊)
    - n=5: index=2 (位置2是真正的中位數)

3️⃣  計算距離：逐一計算每個親戚到中位數位置的距離
    距離公式：|親戚位置 - 中位數位置|

4️⃣  求和：將所有距離加起來得到最小距離總和

時間複雜度分析:
--------------
• 排序：O(n log n) ← 這是主要的耗時操作
• 找中位數：O(1)
• 計算距離：O(n)
• 總時間複雜度：O(n log n)

空間複雜度分析:
--------------
• 只使用常數個額外變數
• 總空間複雜度：O(1)（不計算輸入的空間）

====================================================================
                         具體例子
====================================================================

例子 1：奇數個親戚
-----------------
親戚位置: [10, 40, 20]
步驟 1 - 排序：[10, 20, 40]
步驟 2 - 中位數：index = 3//2 = 1，中位數 = 20
步驟 3 & 4 - 計算距離：
  |10-20| + |20-20| + |40-20| = 10 + 0 + 20 = 30
結果：30

例子 2：偶數個親戚
-----------------
親戚位置: [1, 2, 3, 4]
步驟 1 - 排序：[1, 2, 3, 4]（已排序）
步驟 2 - 中位數：index = 4//2 = 2，中位數 = 3
步驟 3 & 4 - 計算距離：
  |1-3| + |2-3| + |3-3| + |4-3| = 2 + 1 + 0 + 1 = 4
結果：4

驗證：如果選擇位置 2（另一個中間值）：
  |1-2| + |2-2| + |3-2| + |4-2| = 1 + 0 + 1 + 2 = 4
同樣的結果！這證實了偶數情況下兩個中間值都是最優的。

例子 3：有重複位置
-----------------
親戚位置: [5, 5, 5, 10]
步驟 1 - 排序：[5, 5, 5, 10]（已排序）
步驟 2 - 中位數：index = 4//2 = 2，中位數 = 5
步驟 3 & 4 - 計算距離：
  |5-5| + |5-5| + |5-5| + |10-5| = 0 + 0 + 0 + 5 = 5
結果：5

====================================================================
                         輸入/輸出格式
====================================================================

輸入格式:
--------
第一行：t（測試資料的組數）
接下來 t 行，每行包含：
  • 第一個數字 r：親戚的數量
  • 接下來 r 個數字：親戚的門牌號碼

輸出格式:
--------
對每組測試資料，輸出一個整數，代表距離總和的最小值

輸入範例:
---------
1
4 4 10 1 4

輸出範例:
---------
9

====================================================================
"""


def find_minimum_distance(relatives):
    """
    計算從最優位置到所有親戚房子的最小距離總和

    參數:
    ----
    relatives: list[int]
        親戚房子的門牌號碼列表
        • 不需要事先排序，函式會自動排序
        • 可以包含重複的號碼（多個親戚住在同一位置）
        • 範圍：0 < 號碼 < 30000
        • 親戚數量：0 < 數量 < 500

    返回值:
    ------
    int：距離總和的最小值

    演算法思路:
    ---------
    這個函式實現了「中位數法則」：
    • 對於一維直線上的一組點
    • 到所有點距離最小的位置就是這些點的中位數
    • 因此我們只需要：
      1. 找到中位數
      2. 計算到中位數的距離總和

    為什麼有效:
    --------
    假設有親戚在位置 A, B, C，其中 A ≤ B ≤ C：
    • 如果選擇位置 B（中位數）
      距離 = (B-A) + 0 + (C-B) = C-A
    • 如果選擇位置 B 左邊的任何位置 X (X < B)
      距離 = (B-X) + (B-X) + (C-X) = C + 2B - 3X > C-A
    • 同理，選擇 B 右邊的位置也不會更優

    因此，中位數給出最優解！
    """

    # ============================================================
    # 步驟 1：排序親戚的位置
    # ============================================================
    # 為什麼需要排序？
    # • 排序後我們能輕鬆找到中位數
    # • 排序還能幫助我們理解數據的分佈
    #
    # 時間複雜度：O(n log n)
    # 其中 n 是親戚的數量
    relatives.sort()

    # ============================================================
    # 步驟 2：找出中位數位置
    # ============================================================
    # 中位數是排序後的「中間值」
    #
    # 計算中位數索引的公式：index = len(relatives) // 2
    # 為什麼是 len // 2 而不是 (len-1) // 2？
    #
    # 舉例說明：
    # • 長度 1: [a]                    → index=0，a 是唯一的點 ✓
    # • 長度 2: [a, b]                 → index=1，選擇 b（右邊中間值）✓
    # • 長度 3: [a, b, c]              → index=1，b 是真中位數 ✓
    # • 長度 4: [a, b, c, d]           → index=2，c 是右邊中間值 ✓
    # • 長度 5: [a, b, c, d, e]        → index=2，c 是真中位數 ✓
    #
    # 對於偶數長度，選擇右邊的中間值同樣給出最優解
    median_index = len(relatives) // 2

    # 獲取中位數的實際值（門牌號碼）
    median_value = relatives[median_index]

    # ============================================================
    # 步驟 3：計算距離總和
    # ============================================================
    # 逐一計算每個親戚到中位數位置的距離，然後累加
    #
    # 距離公式：|親戚位置 - 中位數位置|
    # 絕對值確保距離永遠是正數，不論親戚在左邊還是右邊
    #
    # 時間複雜度：O(n)
    # 其中 n 是親戚的數量

    total_distance = 0  # 累計距離

    for relative_position in relatives:
        # 計算這個親戚到中位數的距離
        distance = abs(relative_position - median_value)

        # 加到總距離上
        total_distance += distance

    # ============================================================
    # 步驟 4：返回結果
    # ============================================================
    # 返回計算出的最小距離總和
    return total_distance


# ====================================================================
# 用於線上評測的輸入處理函式（可選）
# ====================================================================


def read_and_process():
    """
    從標準輸入讀取測試資料並輸出結果

    這個函式用於線上評測系統，不用於 unit 測試。

    讀取的數據格式：
    • 第一行：測試組數 t
    • 接下來 t 行，每行格式為：
      r s₁ s₂ ... sᵣ
      其中 r 是親戚數量，s₁ 到 sᵣ 是門牌號碼

    例如：
    1
    4 4 10 1 4

    這表示：1 組測試資料，有 4 個親戚，位置分別是 4, 10, 1, 4
    """

    # 讀取測試資料的組數
    t = int(input())

    # 依次處理每一組測試資料
    for _ in range(t):
        # 讀取一整行，並轉換為整數列表
        line = list(map(int, input().split()))

        # 第一個數字是親戚數量
        r = line[0]

        # 後續的 r 個數字是親戚的位置
        # 注意：使用 line[1:r+1] 確保只取前 r 個親戚位置
        relatives = line[1 : r + 1]

        # 計算並輸出這組測試的結果
        result = find_minimum_distance(relatives)
        print(result)


