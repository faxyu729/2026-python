"""
UVA 10056 - 輪流擲骰子遊戲的獲勝機率（詳細註解版本）

====================================================================
                         問題完整描述
====================================================================

遊戲規則:
--------
• 有N個玩家，按順序 1, 2, 3, ..., N 輪流擲骰子
• 每次擲骰子成功的機率為 p
• 當某玩家成功時，該玩家獲勝，遊戲停止
• 如果玩家N失敗，遊戲回到玩家1
• 重複直到有玩家成功

目標:
----
計算第 i 名玩家獲勝的機率

實際例子:
--------
假設 N=2 個玩家，每次成功機率 p=0.5

第一輪:
  玩家1擲骰子: 50%成功 → 玩家1獲勝
  玩家1失敗: 50%概率，玩家2擲骰子: 50%成功 → 玩家2獲勝
  玩家2失敗: 25%概率，回到第一輪

玩家1獲勝的所有方式:
  1. 第1輪玩家1成功: 0.5
  2. 第1輪都失敗，第2輪玩家1成功: 0.25 × 0.5 = 0.125
  3. 第2輪都失敗，第3輪玩家1成功: 0.0625 × 0.5 = 0.03125
  ...
  總和 = 0.5 × (1 + 0.25 + 0.0625 + ...)
       = 0.5 / (1 - 0.25)
       = 0.5 / 0.75
       = 2/3 ≈ 0.6667

====================================================================
                         數學推導
====================================================================

定義:
----
• p: 單次成功機率
• q = 1 - p: 單次失敗機率
• P(i): 第i名玩家的獲勝機率

推導過程:
--------
第i名玩家在某一輪中獲勝需要:
  1. 玩家 1 到 i-1 都失敗（概率 q^(i-1)）
  2. 玩家 i 成功（概率 p）
  單輪獲勝概率 = p × q^(i-1)

但是如果整輪 N 個玩家都失敗了（概率 q^n），遊戲會重新開始。
因此總的獲勝概率是無限級數的和：

P(i) = p × q^(i-1) × (1 + q^n + (q^n)^2 + ...)
     = p × q^(i-1) × 1/(1 - q^n)
     = p × q^(i-1) / (1 - q^n)

其中:
• q^(i-1): 前 i-1 個玩家都失敗
• 1/(1 - q^n): 無限級數求和（等比級數）

====================================================================
                         具體計算例子
====================================================================

例子 1: N=1, p=0.5, i=1
-------
只有一個玩家，不斷擲到成功
P(1) = 0.5 × 0.5^0 / (1 - 0.5^1)
     = 0.5 × 1 / (1 - 0.5)
     = 0.5 / 0.5
     = 1.0 ✓

例子 2: N=2, p=0.5, i=1
-------
兩個玩家，玩家1在奇數輪獲勝
P(1) = 0.5 × 0.5^0 / (1 - 0.5^2)
     = 0.5 × 1 / (1 - 0.25)
     = 0.5 / 0.75
     = 2/3 ≈ 0.6667 ✓

例子 3: N=2, p=0.5, i=2
-------
兩個玩家，玩家2在偶數輪獲勝
P(2) = 0.5 × 0.5^1 / (1 - 0.5^2)
     = 0.5 × 0.5 / 0.75
     = 0.25 / 0.75
     = 1/3 ≈ 0.3333 ✓

驗證: P(1) + P(2) = 2/3 + 1/3 = 1 ✓

====================================================================
                         邊界情況討論
====================================================================

1. p = 0（永不成功）:
   沒有人能贏，機率為 0

2. p = 1（必然成功）:
   只有玩家1能贏（第一輪就成功）
   其他玩家機率為 0

3. p = 1/N:
   每個玩家的優勢相同？不是！
   早玩的玩家優勢仍然更大

4. N = 1:
   只有一個玩家，必然獲勝
   P(1) = 1.0

5. N → ∞（玩家很多）:
   後面的玩家獲勝機率接近 0

====================================================================
                         性質分析
====================================================================

性質1: 概率和為1
-------
P(1) + P(2) + ... + P(N) = 1

證明：
所有玩家必然有人在某時刻獲勝

性質2: 單調遞減
-------
P(1) > P(2) > ... > P(N)

原因：先手優勢

性質3: 與 p 無關的關係
-------
P(i) / P(i+1) = 1 / q（與 i 無關，只與 p 有關）

這表示相鄰玩家的概率比總是固定的

====================================================================
"""


def calculate_win_probability(n, p, i):
    """
    計算第i名玩家在輪流擲骰子遊戲中的獲勝機率

    參數:
    ----
    n: int
        玩家數量 (1 ≤ N ≤ 1000)

    p: float
        單次成功機率 (0 ≤ p ≤ 1)
        表示每次擲骰子成功的概率

    i: int
        要計算的玩家編號 (1 ≤ i ≤ N)
        編號從 1 到 N

    返回值:
    ------
    float
        第i名玩家的獲勝機率
        範圍在 0 到 1 之間

    演算法:
    ------
    步驟1: 處理特殊情況
           如果 p = 0，沒有人能贏 → 返回 0
           如果 p = 1，只有玩家1能贏 → 返回 1 if i==1 else 0

    步驟2: 計算失敗概率
           q = 1 - p
           表示單次失敗的概率

    步驟3: 計算分子
           分子 = p × q^(i-1)
           這是玩家i在某一輪中獲勝的概率

    步驟4: 計算分母
           分母 = 1 - q^n
           這是一整輪都失敗的補集
           用於歸一化無限級數

    步驟5: 計算結果
           P(i) = 分子 / 分母

    時間複雜度: O(log N)（求冪運算）
    空間複雜度: O(1)（常數空間）

    數學公式:
    --------
    P(i) = p × (1-p)^(i-1) / (1 - (1-p)^n)

    其中:
    • p: 成功機率
    • (1-p)^(i-1): 前 i-1 個玩家都失敗的概率
    • (1-p)^n: 一整輪 n 個玩家都失敗的概率
    """

    # ============================================================
    # 特殊情況1: 成功機率為0
    # ============================================================
    # 如果永遠無法成功，沒有人能贏
    # 機率為 0
    if p == 0:
        return 0.0

    # ============================================================
    # 特殊情況2: 成功機率為1
    # ============================================================
    # 如果必然成功，只有玩家1能贏（第一輪就成功）
    # 其他玩家永遠沒有機會
    if p == 1.0:
        # 只有玩家1能贏
        if i == 1:
            return 1.0
        else:
            return 0.0

    # ============================================================
    # 通常情況: 0 < p < 1
    # ============================================================

    # 計算失敗概率
    # q = 1 - p
    # 這是每次擲骰子失敗的概率
    q = 1 - p

    # 計算分子部分
    # 分子 = p × (1-p)^(i-1)
    # 這表示：前i-1個玩家都失敗，第i個玩家成功的概率
    # 範圍: 0 < 分子 < p
    #
    # 例如 N=3, i=2, p=0.5:
    #   分子 = 0.5 × 0.5^(2-1) = 0.5 × 0.5 = 0.25
    numerator = p * (q ** (i - 1))

    # 計算分母部分
    # 分母 = 1 - (1-p)^n
    # 這表示：至少有一個玩家在某一輪成功的概率
    # 等價於：一整輪都失敗的補集
    #
    # 使用等比級數求和：
    # P(i) = p×q^(i-1) × (1 + q^n + (q^n)^2 + ...)
    #      = p×q^(i-1) / (1 - q^n)
    #
    # 例如 N=2, p=0.5:
    #   分母 = 1 - 0.5^2 = 1 - 0.25 = 0.75
    denominator = 1 - (q**n)

    # ============================================================
    # 計算最終結果
    # ============================================================
    # P(i) = 分子 / 分母
    # 這給出第i名玩家的真實獲勝機率
    result = numerator / denominator

    return result


# ====================================================================
# 輸入/輸出處理
# ====================================================================


def read_and_process_input():
    """
    從標準輸入讀取並處理測試資料

    輸入格式說明:
    -----------
    第一行: S（測試資料組數，S ≤ 1000）

    接下來S行，每行包含：
      • N: 玩家數量 (1 ≤ N ≤ 1000)
      • p: 單次成功機率 (0 ≤ p ≤ 1，浮點數)
      • i: 要計算的玩家編號 (1 ≤ i ≤ N)

    輸出格式說明:
    -----------
    對每個測試資料，輸出一行：
      • 第i名玩家的獲勝機率
      • 精確到小數點後4位
      • 格式: f"{result:.4f}"

    範例:
    ----
    輸入:
    3
    2 0.5 1
    2 0.5 2
    3 0.3333 1

    輸出:
    0.6667
    0.3333
    0.4737

    解釋:
    • 2個玩家，50%成功，玩家1 → 機率 2/3
    • 2個玩家，50%成功，玩家2 → 機率 1/3
    • 3個玩家，1/3成功，玩家1 → 機率約 0.4737
    """

    # 讀取測試資料組數
    s = int(input())

    # 處理每一組測試資料
    for _ in range(s):
        # 讀取 N, p, i
        # 注意：需要分別解析，因為 p 是浮點數
        parts = input().split()
        n = int(parts[0])
        p = float(parts[1])
        i = int(parts[2])

        # 計算第i名玩家的獲勝機率
        result = calculate_win_probability(n, p, i)

        # 輸出結果，精確到4位小數
        print(f"{result:.4f}")


